public class Ex014 {
    public static void main(String[] args){
        // Loop For
        // Executa um bloco de código durante um intervalo finito
        // Começo, Fim e Passo
        for(int i = 10; i >= 1; i--) { // Podemos usar o passo da seguinte forma: i-=2
            System.out.println(i);
        }
        System.out.print("Feliz ano novo! \uD83C\uDF86");
    }
}
