public class Ex001 {
    // Classe é uma coleção de códigos
    public static void main(String[] args) {
        // Todo projeto Java deve ter um MÉTODO principal
        // Se não houver método principal, a classe irá apresentar erro
        System.out.println("Hello, World!");
        // É possível usar um \n para pular, ele é chamado de Sequência de Escape
        System.out.println("This is my first program in Java :)\nLet's code together!");
        System.out.println("\tThis is an example of the tab escape sequence.");
        // Printando com aspas
        System.out.println("\"I love pizza\"");
        System.out.print("\\Double backslashes, to print one!");
        /*
         * Comentários
         * em múltiplas
         * linhas
         */
    }
}